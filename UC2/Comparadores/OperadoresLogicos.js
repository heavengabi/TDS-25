//Operadores Lógicos 

//valores exemplo
let a = true
let b = false

console.log("Operadores Lógicos em JavaScript")

//AND (&&)  Retorna se os dois forem true

console.log ("true && true=>", true && true)
console.log ("true && false=>", true && false)
console.log ("a && b =>", a && b)


//OR (||) Rtorna true se pelo menos um for true
console.log("true || false=>", true || false)
console.log("false || false", false || false) 
console.log (" a || b =>", a || b )

//NOT (!) Inverte o valor do booleano

console.log("!true =>", !true)
console.log("!false =>", !false )
console.log("!a =>", !a)
console.log ("!b =>", !b)

//Combinações mais complexas
console.log("! (a && b) =>", !(a && b))