function redeSocialFav()
{

let pergunta= prompt("Qual rede social você mais usa: instagram, tiktok, twitter, facebook ou linkedin").toLowerCase()

switch (pergunta)

{

case ("instagram"):
    alert("Perfeito para fotos")
    break

case("tiktok"):
    alert("Vídeos curtos e muitas danças.")
    break

case("twitter"):
    alert("O lugar das notícias rápidas e memes")
    break

case ("facebook"):
    alert("Clássico, mas ainda forte para grupos")
    break

case("linkedin"):
    alert("Rede profissional e network")
    break

    
}

}


redeSocialFav()