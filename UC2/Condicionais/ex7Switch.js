
let dia = Number(prompt("Digite um número de 1 a 5 para o dia da semana: "))
switch(dia)
{
    case 1:
        alert("Segunda-Feira: mais um dia, lindo dia..")
        break
    case 2: 
        alert("Terça-Feira: vamos em frente..")
        break
    case 3: 
        alert("Quarta-Feira: chegamos ao meio da semana...sabádo está quase ai")
        break
    case 4: 
        alert("Quinta-Feira: Quase sexta...aguenta aí")

    case 5: 
        alert("Sexta-Feira: FINALMENTE!!")
    
    default:
        alert("Dia inválido")
}